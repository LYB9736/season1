                  final image = fruit.images[index];
                  return Container(
                    width: 410, // 이미지 컨테이너의 너비 설정
                    height: 10, // 이미지 컨테이너의 높이 설정
                    child: Image.asset(
                      image,
                      fit: BoxFit.cover, // 이미지를 부모 위젯에 꽉 채움
                    ),
                  );
                },
              ),
            ),
            SizedBox(height: 16),
            Text(
              '계절: ${fruit.season}',
              style: TextStyle(
                fontSize: 18,
              ),
            ),
            SizedBox(height: 16),
            Text(
              fruit.description,
              style: TextStyle(
                fontSize: 18,
                fontFamily: 'Lato',
              ),
            ),
            SizedBox(height: 16),
            ElevatedButton(
              child: Text('뒤로 가기'),
