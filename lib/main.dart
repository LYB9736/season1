class HomeScreen extends StatefulWidget {
  @override
  _HomeScreenState createState() => _HomeScreenState();
}

class _HomeScreenState extends State<HomeScreen> {
  String selectedSeason = '여름(5월 ~ 8월)';

  List<Fruit> getFilteredFruits() {
    return fruits.where((fruit) => fruit.season == selectedSeason).toList();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text('계절별 과일'),
      ),
      body: Column(
        children: [
          Padding(
            padding: const EdgeInsets.symmetric(vertical: 16),
            child: Row(
              mainAxisAlignment: MainAxisAlignment.spaceEvenly,
              children: [
                SeasonButton(
                  text: '여름',
                  isSelected: selectedSeason == '여름(5월 ~ 8월)',
                  onPressed: () {
                    setState(() {
                      selectedSeason = '여름(5월 ~ 8월)';
                    });
                  },
                ),
                SeasonButton(
                  text: '가을',
                  isSelected: selectedSeason == '가을(9월 ~ 11월)',
                  onPressed: () {
                    setState(() {
                      selectedSeason = '가을(9월 ~ 11월)';
                    });
                  },
                ),
                SeasonButton(
                  text: '겨울',
                  isSelected: selectedSeason == '겨울(12월 ~ 2월)',
                  onPressed: () {
                    setState(() {
                      selectedSeason = '겨울(12월 ~ 2월)';
                    });
                  },
                ),
              ],
            ),
          ),
          Expanded(
            child: GridView.builder(
              padding: EdgeInsets.all(16),
              itemCount: getFilteredFruits().length,
              gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
                crossAxisCount: 1,
                crossAxisSpacing: 16,
                mainAxisSpacing: 16,
              ),
              itemBuilder: (context, index) {
                final fruit = getFilteredFruits()[index];
                return GestureDetector(
                  onTap: () {
                    Navigator.pushNamed(
                      context,
                      '/detail',
                      arguments: fruit,
                    );
                  },
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.stretch,
                    children: [
                      Expanded(
                        child: Image.asset(
                          fruit.images[0],
                          fit: BoxFit.cover,
                        ),
                      ),
                      SizedBox(height: 8),
                      Text(
                        fruit.name,
                        textAlign: TextAlign.center,
                        style: TextStyle(
                          fontWeight: FontWeight.bold,
                        ),
                      ),
                      SizedBox(height: 4),
                      Text(
                        fruit.season,
                        textAlign: TextAlign.center,
                        style: TextStyle(
                          color: Colors.grey[600],
                        ),
                      ),
                    ],
                  ),
                );
              },
            ),
          ),
        ],
      ),
    );
  }
}

class SeasonButton extends StatelessWidget {
  final String text;
  final bool isSelected;
  final VoidCallback onPressed;

  const SeasonButton({
    required this.text,
    required this.isSelected,
    required this.onPressed,
  });

  @override
  Widget build(BuildContext context) {
    return ElevatedButton(
      onPressed: onPressed,
      style: ButtonStyle(
        backgroundColor: isSelected ? MaterialStateProperty.all(Colors.green) : null,
      ),
      child: Text(
        text,
        style: TextStyle(
          color: isSelected ? Colors.white : null,
        ),
      ),
    );
  }
}

